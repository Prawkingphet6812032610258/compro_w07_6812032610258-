#include <stdio.h> // เรียกใช้ไลบรารีมาตรฐานสำหรับ Input/Output

int main() {
    // ประกาศตัวแปรชนิด integer (จำนวนเต็ม) เพื่อเก็บค่าตัวเลข 2 จำนวน
    int x, y;

    // --- ส่วนของการรับค่าจากผู้ใช้ ---
    printf("กรุณาป้อนตัวเลขจำนวนที่ 1 (x): "); // แสดงข้อความเพื่อบอกให้ผู้ใช้ป้อนค่า
    scanf("%d", &x); // รับค่าจากผู้ใช้และเก็บไว้ในตัวแปร x

    printf("กรุณาป้อนตัวเลขจำนวนที่ 2 (y): "); // แสดงข้อความเพื่อบอกให้ผู้ใช้ป้อนค่า
    scanf("%d", &y); // รับค่าจากผู้ใช้และเก็บไว้ในตัวแปร y

    // --- ส่วนของการเปรียบเทียบค่าและแสดงผลลัพธ์ ---
    // ใช้คำสั่ง if-else if-else เพื่อเปรียบเทียบค่า
    if (x > y) { // ตรวจสอบเงื่อนไขแรก: ถ้า x มากกว่า y
        printf("ผลลัพธ์: x มีค่ามากกว่า y\n"); // แสดงผลเมื่อเงื่อนไขเป็นจริง
    } else if (x < y) { // ถ้าเงื่อนไขแรกไม่เป็นจริง ให้ตรวจสอบเงื่อนไขที่สอง: ถ้า x น้อยกว่า y
        printf("ผลลัพธ์: x มีค่าน้อยกว่า y\n"); // แสดงผลเมื่อเงื่อนไขที่สองเป็นจริง
    } else { // ถ้าเงื่อนไขข้างต้นทั้งหมดไม่เป็นจริง แสดงว่า x เท่ากับ y
        printf("ผลลัพธ์: x มีค่าเท่ากับ y\n"); // แสดงผลเมื่อเงื่อนไขเป็นเท็จทั้งหมด
    }

    return 0; // คืนค่า 0 เพื่อแสดงว่าโปรแกรมทำงานเสร็จสมบูรณ์โดยไม่มีข้อผิดพลาด
}
//การอธิบายการทำงานของโปรแกรม
//#include <stdio.h>: บรรทัดนี้เป็นการ เรียกใช้ไลบรารี stdio.h ซึ่งเป็นไลบรารีมาตรฐานที่จำเป็นสำหรับการทำงานเกี่ยวกับ Input (รับค่า) และ Output (แสดงผล) เช่นคำสั่ง printf() และ scanf()
//int main(): นี่คือ ฟังก์ชันหลัก ที่โปรแกรมจะเริ่มทำงานที่นี่เป็นอันดับแรก
//int x, y;: เป็นการ ประกาศตัวแปร x และ y ซึ่งใช้สำหรับเก็บข้อมูลที่เป็น จำนวนเต็ม (int)
//printf("..."): ใช้สำหรับ แสดงข้อความ บนหน้าจอเพื่อสื่อสารกับผู้ใช้
//scanf("%d", &x);: เป็นคำสั่งสำหรับ รับค่า จากแป้นพิมพ์ โดย %d หมายถึงรับข้อมูลที่เป็น จำนวนเต็ม และ &x คือการระบุตำแหน่งหน่วยความจำของตัวแปร x เพื่อเก็บค่าที่รับเข้ามา
//if (x > y): เป็น เงื่อนไขแรก ถ้า x มีค่ามากกว่า y โปรแกรมจะทำคำสั่งที่อยู่ในบล็อก if
//else if (x < y): ถ้าเงื่อนไขแรกเป็นเท็จ โปรแกรมจะมาตรวจสอบ เงื่อนไขที่สอง ถ้า x มีค่าน้อยกว่า y โปรแกรมจะทำคำสั่งในบล็อก else if
//else: ถ้าเงื่อนไขข้างต้นทั้งหมดเป็นเท็จ นั่นหมายความว่า x จะต้องมีค่าเท่ากับ y โปรแกรมจะทำคำสั่งในบล็อก else นี้
//return 0;: เป็นการบอกระบบปฏิบัติการว่าโปรแกรมทำงานเสร็จสมบูรณ์แล้ว