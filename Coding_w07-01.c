#include <stdio.h> // ใช้สำหรับฟังก์ชัน input/output เช่น printf, scanf

int main() {
    // 1. ส่วนของการประกาศตัวแปร
    float midtermScore; // ตัวแปรสำหรับเก็บคะแนนสอบ midterm
    float finalScore; // ตัวแปรสำหรับเก็บคะแนนรวมสุดท้าย

    // 2. ส่วนของการรับข้อมูล (Input)
    printf("กรุณาใส่คะแนนสอบ midterm ของคุณ: ");
    scanf("%f", &midtermScore); // รับค่าคะแนนจากผู้ใช้และเก็บในตัวแปร midtermScore

    // 3. ส่วนของการตรวจสอบเงื่อนไขและคำนวณ (Decision & Process)
    // ใช้คำสั่ง if เพื่อตรวจสอบว่าคะแนนมากกว่าหรือเท่ากับ 50 หรือไม่
    if (midtermScore >= 50) {
        // ถ้าเงื่อนไขเป็นจริง (คะแนน >= 50)
        printf("คุณได้รับคะแนนพิเศษ 5%% เนื่องจากคะแนนสอบของคุณผ่านเกณฑ์!\n");
        // คำนวณคะแนนรวมใหม่โดยเพิ่มคะแนนพิเศษ 5%
        finalScore = midtermScore * 1.05; 
        /* การคูณด้วย 1.05 เทียบเท่ากับการนำคะแนนเดิม (midtermScore) มาบวกกับ
           คะแนนพิเศษ (midtermScore * 0.05) ซึ่งทำให้โค้ดสั้นและเข้าใจง่ายขึ้น
        */
    } else {
        // ถ้าเงื่อนไขเป็นเท็จ (คะแนน < 50)
        printf("คะแนนของคุณไม่ผ่านเกณฑ์สำหรับคะแนนพิเศษ\n");
        // คะแนนสุดท้ายเท่ากับคะแนนเดิม
        finalScore = midtermScore; 
    }

    // 4. ส่วนของการแสดงผลลัพธ์ (Output)
    // แสดงผลคะแนนรวมสุดท้าย โดยใช้ %.2f เพื่อแสดงทศนิยม 2 ตำแหน่ง
    printf("คะแนนรวมสุดท้ายของคุณคือ: %.2f คะแนน\n", finalScore);

    // 5. ส่วนของการสิ้นสุดโปรแกรม
    printf("End of evaluation\n");

    return 0; // คืนค่า 0 เพื่อบอกว่าโปรแกรมทำงานเสร็จสมบูรณ์
}
