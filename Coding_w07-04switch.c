//โครงสร้างแบบ switch-case
#include <stdio.h>

void showBenefits_switch(int level) {
    switch (level) {
        case 1:
            printf("Silver Member: 5%% discount\n");
            break;
        case 2:
            printf("Gold Member: 10%% discount + Reward points\n");
            break;
        case 3:
            printf("Platinum Member: 15%% discount + Reward points + Birthday gift\n");
            break;
        case 4:
            printf("Diamond Member: All benefits + VIP events\n");
            break;
        default:
            printf("Invalid membership level\n");
            break;
    }
}

int main() {
    int membershipLevel;

    printf("Enter membership level (1-4): ");
    scanf("%d", &membershipLevel);

    showBenefits_switch(membershipLevel);

    return 0;
}
//คำอธิบาย
// โค้ดนี้ใช้ฟังก์ชัน showBenefits_switch และใช้คำสั่ง switch เพื่อตรวจสอบค่าของ level
// คำสั่ง case แต่ละอันจะจับคู่กับค่าของ level หากตรงกันก็จะทำงานในบล็อกนั้น
// คำสั่ง break เป็นสิ่งสำคัญที่ต้องใช้ในแต่ละ case เพื่อหยุดการทำงานเมื่อเงื่อนไขตรงกัน และป้องกันไม่ให้โค้ดรันไปใน case ถัดไป (fall-through)
// default จะทำงานในกรณีที่ค่าของ level ไม่ตรงกับ case ใดๆ เลย เปรียบเสมือนกับบล็อก else ในโครงสร้าง if-else if
//การวิเคราะห์และเปรียบเทียบ
//1. ความยืดหยุ่น (Flexibility)
// if-else if: มีความยืดหยุ่นสูงกว่า สามารถใช้ตรวจสอบเงื่อนไขที่ซับซ้อนได้ เช่น การเปรียบเทียบช่วงค่า (x > 10 และ x < 20) หรือเงื่อนไขที่ประกอบด้วยหลายตัวแปรพร้อมกัน
// switch-case: เหมาะสำหรับการตรวจสอบ ค่าคงที่ ของตัวแปรเดียวเท่านั้น ไม่สามารถใช้กับช่วงค่าหรือเงื่อนไขที่ซับซ้อนได้
//2. ความกระชับและอ่านง่าย (Conciseness & Readability)
// if-else if: ในกรณีที่มีเงื่อนไขจำนวนมาก โครงสร้างโค้ดจะเริ่มยาวและซับซ้อน ทำให้การอ่านและทำความเข้าใจยากขึ้น (โดยเฉพาะเมื่อเงื่อนไขแต่ละอันมีความซับซ้อน)
// switch-case: เหมาะสำหรับกรณีที่มีการตรวจสอบค่าคงที่จำนวนมาก โครงสร้างจะดูเป็นระเบียบและอ่านง่ายกว่ามาก เนื่องจากแต่ละ case จะแสดงถึงค่าที่ต้องการตรวจสอบอย่างชัดเจน
//3. ประสิทธิภาพ (Performance)
// ในทางทฤษฎี switch-case มักจะมีประสิทธิภาพดีกว่าในบางกรณี เนื่องจากคอมไพเลอร์สามารถสร้างตารางกระโดด (jump table) เพื่อเข้าถึง case ที่ต้องการได้โดยตรง ซึ่งเร็วกว่าการตรวจสอบเงื่อนไขแบบทีละขั้นของ if-else if อย่างไรก็ตามในโปรแกรมขนาดเล็กแบบนี้ ความแตกต่างของประสิทธิภาพแทบจะไม่มีนัยสำคัญเลย
//สรุปความเหมาะสมกับโจทย์
//สำหรับโจทย์นี้ switch-case เป็นวิธีที่ เหมาะสมกว่า 
//เหตุผล
// โจทย์นี้เป็นการตรวจสอบ ค่าคงที่ (1, 2, 3, 4) ของตัวแปร membershipLevel ซึ่งเป็นสถานการณ์ที่ switch-case ถูกออกแบบมาเพื่อจัดการโดยเฉพาะ
// โค้ดที่ใช้ switch-case จะดู เป็นระเบียบและอ่านง่าย กว่ามากเมื่อมีการเพิ่มระดับสมาชิกในอนาคต ทำให้ maintain (บำรุงรักษา) ได้ง่ายกว่า
// การใช้ break ในแต่ละ case ช่วยให้มั่นใจได้ว่าโปรแกรมจะทำงานเฉพาะในส่วนที่ต้องการเท่านั้น
//แม้ว่า if-else if จะสามารถใช้แก้ปัญหานี้ได้เช่นกัน แต่เมื่อพิจารณาจากข้อกำหนดของโจทย์ที่ต้องการโค้ดที่อ่านง่ายและแก้ไขสะดวกในอนาคต switch-case จึงเป็นตัวเลือกที่ดีที่สุดและเป็น Best Practice สำหรับสถานการณ์แบบนี้